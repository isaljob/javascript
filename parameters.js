// tanpa parameter
function panggilParameter0() {
 console.log('Medan');
}
panggilParameter0();

// 1 parameter
function panggilParameter1(kota1) {
 console.log(kota1);
}
panggilParameter1('Jakarta', 'Medan', 'Balikpapan');

// lebih dari 1 parameter
function panggilParameter2(kota1, kota2, kota3) {
 console.log(kota1);
 console.log(kota2);
 console.log(kota3);
}
panggilParameter2('Jakarta', 'Medan', 'Balikpapan');
